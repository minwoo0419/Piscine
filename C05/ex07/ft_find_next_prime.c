int	ft_find_next_prime(int nb)
{
	int	i;
	int	j;

	i = nb;
	j = 2;
	while (j * j <= i)
	{
		if (i % j == 0)
		{
			i++;
			j = 2;
		}
		else
			j++;
	}
	return (i);
}
