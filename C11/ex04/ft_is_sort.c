int	ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	int	i;
	int	j;

	j = f(tab[0], tab[1]);
	i = 2;
	while (tab[i])
	{
		if (j != f(tab[i - 1], tab[i]))
			return (0);
		i++;
	}
	return (1);
}
