int	ft_count_if(char **tab, int length, int (*f)(char*))
{
	int	i;
	int	cnt;

	i = 0;
	cnt = 0;
	while (tab[i])
	{
		if (f(tab[i]))
			cnt++;
		i++;
	}
	return (cnt);
}
